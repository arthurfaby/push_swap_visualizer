#include "libft.h"

int	ft_putstr(char *s)
{
	int	ret;

	if (!s)
	{
		ft_putstr("(null)");
		return (6);
	}
	ret = ft_strlen(s);
	while (*s)
		write(1, s++, 1);
	return (ret);
}
